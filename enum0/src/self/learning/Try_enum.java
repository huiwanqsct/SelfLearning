package self.learning;

// 枚举类型的使用
public class Try_enum 
{	
	public static void main(String[] args)
	{
		for (Money m : Money.values())
		{
			System.out.println(m + ", ordinal " + m.ordinal());
		}
		Money m = Money.YI;
		switch(m)
		{
		case YI: System.out.println(m.getName()); break;
		case ER: System.out.println(m.getName()); break;
		case WU: System.out.println(m.getName()); break;
		case SY: System.out.println(m.getName()); break;
		case ES: System.out.println(m.getName()); break;
		case WS: System.out.println(m.getName()); break;
		}
	}
}

